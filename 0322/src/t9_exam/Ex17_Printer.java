package t9_exam;

public class Ex17_Printer {
	
	public static void println(int su) {
		System.out.println(su);
	}
	
	public static void println(boolean su) {
		System.out.println(su);
	}
	
	public static void println(double su) {
		System.out.println(su);
	}
	
	public static void println(String su) {
		System.out.println(su);
	}
	
}
